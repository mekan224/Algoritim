# Циклические алгоритмы и операторы циклов

# Задание 1. Цикл с предусловием
x = int(input("Введите целое число: "))
while x <= 0:
    x = int(input("Введите положительное число: "))
print("Корректное число:", x)

# Задание 2. Цикл с постусловием (имитация)
while True:
    x = int(input("Введите число больше 100: "))
    if x > 100:
        break

# Задание 3. Цикл со счётчиком
n = int(input("Введите n: "))
s = 0
for i in range(1, n + 1):
    s += i
print("Сумма от 1 до n:", s)

# Задание 4. Вложенные циклы (таблица умножения)
for i in range(1, 6):
    for j in range(1, 6):
        print(i * j, end=" ")
    print()

# Задание 5. Исправление ошибки
